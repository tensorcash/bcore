// Complete test vectors
struct TestVector {
    const char* name;
    const char* description;

    // Proof components (A, B, C)
    const uint8_t* proof_a;  // 48 bytes
    const uint8_t* proof_b;  // 96 bytes
    const uint8_t* proof_c;  // 48 bytes

    // Public inputs (4 elements * 32 bytes = 128 bytes)
    const uint8_t* public_inputs;
    size_t public_inputs_size;

    // Verifying key (variable size)
    const uint8_t* verifying_key;
    size_t verifying_key_size;

    // Context
    int current_height;
    uint32_t max_root_age;
    const uint8_t* expected_anchor;  // 32 bytes

    // Expected result
    const char* expected_result;
};

